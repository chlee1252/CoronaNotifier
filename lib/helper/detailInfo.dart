class DetailInfo {
  int active;
  int death;
  int newActive;
  int newDeath;
  String county;
  String date;

  DetailInfo({int active, int death, int newActive, int newDeath, String county, String date}) {
    this.active = active;
    this.death = death;
    this.newActive = newActive;
    this.newDeath = newDeath;
    this.county = county;
    this.date = date;
  }
}
